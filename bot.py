# bot.py
# Telegram-–±–æ—Ç –¥–ª—è —é—Ä–∏–¥–∏—á–µ—Å–∫–∏—Ö –∫–æ–Ω—Å—É–ª—å—Ç–∞—Ü–∏–π –ø–æ –¢–ö –†–§ –∏ –ö–æ–ª–ª–µ–∫—Ç–∏–≤–Ω–æ–º—É –¥–æ–≥–æ–≤–æ—Ä—É –û–ê–û "–†–ñ–î"

# –ò–º–ø–æ—Ä—Ç–∏—Ä—É–µ–º –Ω–µ–æ–±—Ö–æ–¥–∏–º—ã–µ –º–æ–¥—É–ª–∏
import os
import logging
from dotenv import load_dotenv
from telegram import Update
from telegram.ext import Application, CommandHandler, MessageHandler, ContextTypes, filters

# –ò–º–ø–æ—Ä—Ç–∏—Ä—É–µ–º –±–∞–∑—É –∑–Ω–∞–Ω–∏–π –∏–∑ –¥—Ä—É–≥–æ–≥–æ —Ñ–∞–π–ª–∞
from knowledge_base import KNOWLEDGE_BASE


# === –®–ê–ì 1: –ó–∞–≥—Ä—É–∑–∫–∞ —Ç–æ–∫–µ–Ω–∞ –∏–∑ .env ===
load_dotenv()  # –ó–∞–≥—Ä—É–∂–∞–µ—Ç –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ –∏–∑ —Ñ–∞–π–ª–∞ .env
BOT_TOKEN = os.getenv("BOT_TOKEN")  # –ü–æ–ª—É—á–∞–µ–º —Ç–æ–∫–µ–Ω

if not BOT_TOKEN:
    raise ValueError("‚ùå –û—à–∏–±–∫–∞: —Ç–æ–∫–µ–Ω –Ω–µ –Ω–∞–π–¥–µ–Ω! –£–±–µ–¥–∏—Ç–µ—Å—å, —á—Ç–æ –≤ —Ñ–∞–π–ª–µ .env –µ—Å—Ç—å —Å—Ç—Ä–æ–∫–∞ BOT_TOKEN=–≤–∞—à_—Ç–æ–∫–µ–Ω")


# === –®–ê–ì 2: –ù–∞—Å—Ç—Ä–æ–π–∫–∞ –ª–æ–≥–∏—Ä–æ–≤–∞–Ω–∏—è ===
logging.basicConfig(
    format="%(asctime)s - %(name)s - %(levelname)s - %(message)s",
    level=logging.INFO  # –ü–æ–∫–∞–∑—ã–≤–∞–µ—Ç –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏–æ–Ω–Ω—ã–µ —Å–æ–æ–±—â–µ–Ω–∏—è –∏ –æ—à–∏–±–∫–∏
)


# === –®–ê–ì 3: –§—É–Ω–∫—Ü–∏—è –ø–æ–∏—Å–∫–∞ –æ—Ç–≤–µ—Ç–∞ ===
def find_answer(question: str) -> dict | None:
    """
    –ò—â–µ—Ç –æ—Ç–≤–µ—Ç –≤ –±–∞–∑–µ –∑–Ω–∞–Ω–∏–π –ø–æ –∫–ª—é—á–µ–≤—ã–º —Å–ª–æ–≤–∞–º.
    –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç —Å–ª–æ–≤–∞—Ä—å —Å 'answer' –∏ 'source', –ª–∏–±–æ None.
    """
    question_lower = question.lower()  # –ü—Ä–∏–≤–æ–¥–∏–º –≤–æ–ø—Ä–æ—Å –∫ –Ω–∏–∂–Ω–µ–º—É —Ä–µ–≥–∏—Å—Ç—Ä—É
    
    for item in KNOWLEDGE_BASE:
        # –ü—Ä–æ–≤–µ—Ä—è–µ–º, –µ—Å—Ç—å –ª–∏ —Ö–æ—Ç—è –±—ã –æ–¥–Ω–æ –∫–ª—é—á–µ–≤–æ–µ —Å–ª–æ–≤–æ –∏–∑ –∑–∞–ø–∏—Å–∏ –≤ –≤–æ–ø—Ä–æ—Å–µ
        if any(keyword in question_lower for keyword in item["keywords"]):
            return item  # –í–æ–∑–≤—Ä–∞—â–∞–µ–º –ø–µ—Ä–≤—É—é –ø–æ–¥—Ö–æ–¥—è—â—É—é –∑–∞–ø–∏—Å—å
    
    return None  # –ï—Å–ª–∏ –Ω–∏—á–µ–≥–æ –Ω–µ –Ω–∞–π–¥–µ–Ω–æ


# === –®–ê–ì 4: –û–±—Ä–∞–±–æ—Ç—á–∏–∫ –∫–æ–º–∞–Ω–¥—ã /start ===
async def start(update: Update, context: ContextTypes.DEFAULT_TYPE):
    """
    –û—Ç–ø—Ä–∞–≤–ª—è–µ—Ç –ø—Ä–∏–≤–µ—Ç—Å—Ç–≤–µ–Ω–Ω–æ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ –ø—Ä–∏ –∑–∞–ø—É—Å–∫–µ –±–æ—Ç–∞.
    """
    welcome_text = (
        "–ó–¥—Ä–∞–≤—Å—Ç–≤—É–π—Ç–µ! –Ø ‚Äî –±–æ—Ç –ø–æ —Ç—Ä—É–¥–æ–≤–æ–º—É –ø—Ä–∞–≤—É –∏ –∫–æ–ª–ª–µ–∫—Ç–∏–≤–Ω–æ–º—É –¥–æ–≥–æ–≤–æ—Ä—É –û–ê–û ¬´–†–ñ–î¬ª.\n\n"
        "üìå –ó–∞–¥–∞–π—Ç–µ –ª—é–±–æ–π –≤–æ–ø—Ä–æ—Å, –Ω–∞–ø—Ä–∏–º–µ—Ä:\n"
        "‚Ä¢ –ö–∞–∫–∞—è –∏–Ω–¥–µ–∫—Å–∞—Ü–∏—è –∑–∞—Ä–ø–ª–∞—Ç—ã –Ω–∞ –†–ñ–î?\n"
        "‚Ä¢ –ß—Ç–æ –ø–æ–ª–æ–∂–µ–Ω–æ –ø—Ä–∏ —Ä–æ–∂–¥–µ–Ω–∏–∏ —Ä–µ–±—ë–Ω–∫–∞?\n"
        "‚Ä¢ –ú–æ–≥—É—Ç –ª–∏ —É–≤–æ–ª–∏—Ç—å –æ–±–æ–∏—Ö —Å—É–ø—Ä—É–≥–æ–≤?\n"
        "‚Ä¢ –ö–∞–∫–∏–µ –ª—å–≥–æ—Ç—ã —É –ø–µ–Ω—Å–∏–æ–Ω–µ—Ä–æ–≤ –†–ñ–î?\n\n"
        "‚ö†Ô∏è –í–∞–∂–Ω–æ: —è –Ω–µ –∑–∞–º–µ–Ω—è—é —é—Ä–∏—Å—Ç–∞. –î–ª—è —Å–ª–æ–∂–Ω—ã—Ö —Å–∏—Ç—É–∞—Ü–∏–π –æ–±—Ä–∞—Ç–∏—Ç–µ—Å—å –∫ —Å–ø–µ—Ü–∏–∞–ª–∏—Å—Ç—É."
    )
    await update.message.reply_text(welcome_text)


# === –®–ê–ì 5: –û–±—Ä–∞–±–æ—Ç—á–∏–∫ —Ç–µ–∫—Å—Ç–æ–≤—ã—Ö —Å–æ–æ–±—â–µ–Ω–∏–π ===
async def handle_message(update: Update, context: ContextTypes.DEFAULT_TYPE):
    """
    –û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ—Ç –≤—Å–µ —Ç–µ–∫—Å—Ç–æ–≤—ã–µ —Å–æ–æ–±—â–µ–Ω–∏—è –æ—Ç –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è.
    """
    user_question = update.message.text.strip()
    
    # –ó–∞—â–∏—Ç–∞ –æ—Ç –ø—É—Å—Ç—ã—Ö —Å–æ–æ–±—â–µ–Ω–∏–π
    if not user_question:
        return

    # –ò—â–µ–º –æ—Ç–≤–µ—Ç
    result = find_answer(user_question)
    
    if result:
        # –§–æ—Ä–º–∏—Ä—É–µ–º –æ—Ç–≤–µ—Ç —Å –∏—Å—Ç–æ—á–Ω–∏–∫–æ–º
        response = f"{result['answer']}\n\nüìå –ò—Å—Ç–æ—á–Ω–∏–∫: {result['source']}"
    else:
        # –ï—Å–ª–∏ –Ω–∏—á–µ–≥–æ –Ω–µ –Ω–∞–π–¥–µ–Ω–æ ‚Äî –¥—Ä—É–∂–µ–ª—é–±–Ω–æ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ
        response = (
            "–ö —Å–æ–∂–∞–ª–µ–Ω–∏—é, —è –Ω–µ –Ω–∞—à—ë–ª —Ç–æ—á–Ω–æ–≥–æ –æ—Ç–≤–µ—Ç–∞ –≤ —Å–≤–æ–µ–π –±–∞–∑–µ.\n\n"
            "üí° –ü–æ–ø—Ä–æ–±—É–π—Ç–µ:\n"
            "‚Ä¢ –£—Ç–æ—á–Ω–∏—Ç—å –≤–æ–ø—Ä–æ—Å;\n"
            "‚Ä¢ –ò—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å —Å–ª–æ–≤–∞: ¬´–†–ñ–î¬ª, ¬´–∑–∞—Ä–ø–ª–∞—Ç–∞¬ª, ¬´–æ—Ç–ø—É—Å–∫¬ª, ¬´—É–≤–æ–ª—å–Ω–µ–Ω–∏–µ¬ª, ¬´–ª—å–≥–æ—Ç—ã¬ª;\n"
            "‚Ä¢ –ù–∞–ø–∏—Å–∞—Ç—å, —á—Ç–æ –≤—ã —Å–æ—Ç—Ä—É–¥–Ω–∏–∫ –†–ñ–î."
        )
    
    # –û—Ç–ø—Ä–∞–≤–ª—è–µ–º –æ—Ç–≤–µ—Ç –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—é
    await update.message.reply_text(response)


# === –®–ê–ì 6: –ó–∞–ø—É—Å–∫ –±–æ—Ç–∞ ===
def main():
    """
    –û—Å–Ω–æ–≤–Ω–∞—è —Ñ—É–Ω–∫—Ü–∏—è –∑–∞–ø—É—Å–∫–∞ –±–æ—Ç–∞.
    """
    # –°–æ–∑–¥–∞—ë–º –ø—Ä–∏–ª–æ–∂–µ–Ω–∏–µ —Å –≤–∞—à–∏–º —Ç–æ–∫–µ–Ω–æ–º
    app = Application.builder().token(BOT_TOKEN).build()

    # –†–µ–≥–∏—Å—Ç—Ä–∏—Ä—É–µ–º –æ–±—Ä–∞–±–æ—Ç—á–∏–∫–∏
    app.add_handler(CommandHandler("start", start))
    app.add_handler(MessageHandler(filters.TEXT & ~filters.COMMAND, handle_message))

    # –ì–æ—Ç–æ–≤–æ!
    print("‚úÖ –ë–æ—Ç —É—Å–ø–µ—à–Ω–æ –∑–∞–ø—É—â–µ–Ω –∏ –æ–∂–∏–¥–∞–µ—Ç —Å–æ–æ–±—â–µ–Ω–∏–π...")
    print("–û—Ç–∫—Ä–æ–π—Ç–µ Telegram –∏ –Ω–∞–ø–∏—à–∏—Ç–µ —Å–≤–æ–µ–º—É –±–æ—Ç—É!")
    
    # –ó–∞–ø—É—Å–∫–∞–µ–º –æ–ø—Ä–æ—Å (polling)
    app.run_polling()


# === –¢–æ—á–∫–∞ –≤—Ö–æ–¥–∞ ===
if __name__ == "__main__":
    main()